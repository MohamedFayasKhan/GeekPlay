package com.geekcommunity.geekplay;

public class Modal {
    private String path;
    private String name;

    public Modal() {
    }

    public Modal(String path, String name) {
        this.path = path;
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public String getName() {
        return name;
    }
}
